#include <iostream>
#include <windows.h>
#include "labWork6.1-2_string_class.h" // Подключение заголовочного файла с описанием класса MyString

using namespace std;

int main() {
    SetConsoleOutputCP(CP_UTF8); // Для нормального отображения русского языка в консоли

    // Тест конструктора с параметром char*
    MyString s1("Hello");
    MyString s2(" World?");

    // Тест перегруженного оператора сложения (+) и конструктора копирования (при инициализации s3)
    MyString s3 = s1 + s2;

    // Тест перегруженного оператора вывода в поток (<<)
    cout << "Строка1 (s1): " << s1 << endl;
    cout << "Строка2 (s2): " << s2 << endl;
    cout << "Строка3 (s1 + s2): " << s3 << endl;

    // Тест перегруженного оператора сравнения (==)
    if (s1 == s2) {
        cout << "Строка1 равна Строка2" << endl;
    } else {
        cout << "Строка1 не равна Строка2" << endl;
    }

    // Тест метода length()
    cout << "Длина Строка3: " << s3.length() << endl;

    // Тест конструктора по умолчанию и оператора ввода (>>)
    MyString s4;
    cout << "Введите строку (без пробелов, так как cin читает до пробела): ";
    cin >> s4;
    cout << "Вы ввели: " << s4 << endl;

    // Тест оператора присваивания (=)
    MyString s5;
    s5 = s1;
    cout << "Строка5 (копия s1): " << s5 << endl;

    return 0;
}
