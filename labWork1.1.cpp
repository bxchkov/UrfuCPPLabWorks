#include <iostream>
#include <windows.h> // Подключение библиотеки для работы с функциями Windows API (например, для настройки кодировки консоли)

using namespace std; // Использование стандартного пространства имен, чтобы не писать std:: перед cout, cin и т.д.

// Функция exponentiation - возведение числа в степень
// number - основание степени (число, которое возводим)
// exponent - показатель степени (в какую степень возводим)
int exponentiation(int number, int exponent) {
    int i, result = 1; // Инициализация переменной результата единицей (так как умножение на 1 не меняет число, и это нейтральный элемент для умножения)

    // Цикл от 1 до значения exponent включительно
    // Если exponent равно 0, цикл не выполнится ни разу, и вернется result = 1, что верно (любое число в степени 0 равно 1)
    for (i = 1; i <= exponent; i++) {
        result = result * number; // Умножаем текущий результат на основание на каждой итерации
    }

    return result; // Возвращаем вычисленное значение
}

int main() {
    SetConsoleOutputCP(CP_UTF8); // Установка кодировки UTF-8 для корректного отображения русских символов в консоли

    int number, exponent, result;

    // Запрос у пользователя основания степени
    cout << "Введите основание степени\n";
    cin >> number;

    // Запрос у пользователя показателя степени (максимальной)
    cout << "Введите степень\n";
    cin >> exponent;

    // Цикл для вывода всех степеней числа number от 0 до введенного exponent
    for (int i = 0; i <= exponent; i++) {
        // Вызов функции возведения в степень для текущего показателя i
        result = exponentiation(number, i);
        // Вывод результата в формате "основание^степень = результат"
        cout << number << "^" << i << " = " << result << endl;
    }

    return 0; // Завершение программы с кодом 0 (успех)
}
